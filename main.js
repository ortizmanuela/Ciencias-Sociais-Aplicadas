var button = document.querySelector('.botao');
var card_texto = document.querySelector ('.card_texto');

button.addEventListener('click', function(){

    if(card_texto.style.display==='block') {
        card_texto.style.display = 'none';
    } else {
        card_texto.style.display = 'block';
}
});


